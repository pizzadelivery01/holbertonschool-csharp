using System;

namespace MyMath
{
    /// <summary>
    /// class of math operations
    /// </summary>
    public class Operations
    {
        /// <summary>
        /// adds to ints
        /// </summary>
        /// <param name="a">int 1</param>
        /// <param name="b">int 2</param>
        /// <returns>sum of ints</returns>
        public static int Add(int a, int b)
        {
            return (a + b);
        }
    }
}
